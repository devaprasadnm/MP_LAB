;ODD OR EVEN
DATA SEGMENT
    NUM DB ?
    MSG1 DB 10,13,"ENTER THE NUMBER$"
    MSG2 DB 10,13,"INPUT NUMBER IS ODD$"
    MSG3 DB 10,13,"INPUT NUMBER IS EVEN$"
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA
    START:
    MOV AX,DATA
    MOV DS,AX

    LEA DX,MSG1
    MOV AH,09H
    INT 21h


    MOV AH,01H
    INT 21h
    SUB AL,30h
    MOV NUM,AL

    MOV AX,00H
    MOV AL,NUM

    MOV CL,02H
    MOV DH,00H

    DIV CL

    SUB DH,AH

    JZ L1
    LEA DX,MSG2
    MOV AH,09H
    INT 21h

    MOV AH,4CH
    INT 21h

    L1: 
    LEA DX,MSG3
    MOV AH,09H
    INT 21h

    MOV AH,4CH
    INT 21h


CODE ENDS
END START